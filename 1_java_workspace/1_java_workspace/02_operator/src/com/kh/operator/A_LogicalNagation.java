package com.kh.operator;

public class A_LogicalNagation {
	
	//논리부정연산자 ! : 논리 값을 반대로 바꾸는 연산자
	public void method1() {
		boolean bool1 = true;
		boolean bool2 = false;
		
		System.out.println("true의 부정 : " + !true);
		System.out.println("fase의 부정 : " + !false);
		
		System.out.println("true의 부정 : " + !bool1);
		System.out.println("fase의 부정 : " + !bool2);
	}
}
